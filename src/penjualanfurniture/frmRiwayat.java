    private void tblRiwayatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblRiwayatMouseClicked
        // TODO add your handling code here:
        int selectedRow = tblRiwayat.getSelectedRow();
        if (selectedRow != -1) {
            String noTransaksi = tblRiwayat.getValueAt(selectedRow, 0).toString();
            String tanggal = tblRiwayat.getValueAt(selectedRow, 1).toString();
            String namaFurniture = tblRiwayat.getValueAt(selectedRow, 2).toString();
            int jumlahBeli = Integer.parseInt(tblRiwayat.getValueAt(selectedRow, 3).toString());
            int hargaFurniture = Integer.parseInt(tblRiwayat.getValueAt(selectedRow, 4).toString());
            int totalBayar = Integer.parseInt(tblRiwayat.getValueAt(selectedRow, 5).toString());
            
            // Contoh aksi:
            JOptionPane.showMessageDialog(this, 
                "Detail Transaksi:\nNo Transaksi: " + noTransaksi + 
                "\nTanggal: " + tanggal +
                "\nNama Furniture: " + namaFurniture +
                "\nJumlah Beli: " + jumlahBeli +
                "\nHarga Furniture: " + hargaFurniture +
                "\nTotal Bayar: " + totalBayar);
        }


    }//GEN-LAST:event_tblRiwayatMouseClicked

    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
         try {
        // Ambil tanggal dari jDateChooser
         Date tanggalMulai = jDateChooser1.getDate();
            Date tanggalSelesai = jDateChooser2.getDate();
        
        if (tanggalMulai == null || tanggalSelesai == null) {
            JOptionPane.showMessageDialog(this, "Mohon pilih tanggal untuk melakukan pencarian.");
            return;
        }
        
        // Panggil metode pencarian berdasarkan tanggal dari clsRiwayat
        riwayatTransaksi.cariRiwayatByTanggal(tblRiwayat, tanggalMulai, tanggalSelesai);
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan dalam melakukan pencarian: " + e.getMessage());
    }
    }//GEN-LAST:event_btnCariActionPerformed

